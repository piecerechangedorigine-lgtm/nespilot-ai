    private var prefs: SharedPreferences? = null

    fun init(context: Context) {
        prefs = context.getSharedPreferences("settings_prefs", Context.MODE_PRIVATE)
        val defaultLang = when (java.util.Locale.getDefault().language) {
            "ar" -> "AR"
            "en" -> "EN"
            else -> "FR"
        }
        val savedLang = prefs?.getString("language", defaultLang) ?: defaultLang
        _currentLanguage.value = savedLang
    }

    fun changeLanguage(lang: String) {
        _currentLanguage.value = lang
        prefs?.edit()?.putString("language", lang)?.apply()
    }

